<?php

// Pengulangan //

// For
for ($i = 0; $i < 5; $i++) {
  echo "Pengulangan menggunakan for <br>";
}

echo "<br>";

// while
$i = 0;
while ($i < 5) {
  echo "Pengulangan menggunakan while <br>";
  $i++;
}

echo "<br>";

// do..while
$i = 0;
do {
  echo "Pengulangan menggunakan do while <br>";
  $i++;
} while ($i <  5);


// foreach : Pengulangan khusu untuk array